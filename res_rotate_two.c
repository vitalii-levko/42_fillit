void	res_rotate_two(int pos, int *i, int *j)
{
	if (pos == 6)
	{
		*i = *i + 1;
		*j = *j + 1;
	}
	else if (pos == 7)
	{
		*i = *i + 1;
		*j = *j + 2;
	}
	else if (pos == 9)
	{
		*i = *i + 2;
		*j = *j - 1;
	}
	else if (pos == 10)
		*i = *i + 2;
	else if (pos == 11)
	{
		*i = *i + 2;
		*j = *j + 1;
	}
	else if (pos == 15)
		*i = *i + 3;
}
